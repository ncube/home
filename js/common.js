function hideme(e_id) {
	document.getElementById(e_id).style.display = 'none';
}
function c_hideme(e_id) {
	document.getElementsByClassName(e_id)[0].style.display = 'none';
}
function showme(e_id) {
	document.getElementById(e_id).style.display = 'block';
}
function c_showme(e_id) {
	document.getElementsByClassName(e_id)[0].style.display = 'block';
}